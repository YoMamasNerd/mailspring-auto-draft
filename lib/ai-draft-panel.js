/* global AppEnv */

const { React, PropTypes, Actions } = require('mailspring-exports');
const AIService = require('./ai-service');
const { loadThreadContext } = require('./thread-context');
const { textToHtml, htmlToText } = require('./text-utils');

const e = React.createElement;

// Merkt sich Drafts, für die bereits automatisch generiert wurde, damit ein
// Re-Mount des Composers keinen zweiten API-Call auslöst.
const autoGeneratedFor = new Set();

// Marker, vor denen eingefügt wird — identisch zur Logik des internen
// Templates-Plugins von Mailspring.
const INSERTION_MARKERS = [
  '<signature',
  '<div class="gmail_quote_attribution"',
  '<blockquote class="gmail_quote"',
  '<blockquote',
  '<div class="gmail_signature"',
];

// Parser für Tastenkürzel-Konfiguration
function parseShortcut(shortcutStr) {
  if (!shortcutStr || !shortcutStr.trim()) {
    // Default: Strg+Umschalt+G
    return { ctrl: true, shift: true, key: 'g', alt: false, meta: false };
  }
  const parts = shortcutStr.toLowerCase().split('+').map(s => s.trim());
  return {
    ctrl: parts.includes('ctrl') || parts.includes('strg') || parts.includes('control'),
    shift: parts.includes('shift') || parts.includes('umschalt'),
    alt: parts.includes('alt') || parts.includes('option'),
    meta: parts.includes('meta') || parts.includes('cmd') || parts.includes('command') || parts.includes('⌘'),
    key: parts.find(p => !['ctrl','strg','control','shift','umschalt','alt','option','meta','cmd','command','⌘'].includes(p)) || 'g',
  };
}

class AiDraftPanel extends React.Component {
  constructor(props) {
    super(props);
    this.state = {
      status: 'idle',
      suggestion: null,
      partial: '',
      error: null,
      contextInfo: null,
      refineText: '',
      tone: 'standard',
      subjectSuggestion: null,
      collapsed: !!AIService.getConfig('panelCollapsed'),
      // Undo/History
      suggestionHistory: [],
      historyIndex: -1,
      // Health Check / Failover
      healthStatus: 'unknown', // unknown | healthy | unhealthy | checking
      healthError: null,
    };
    this._mounted = false;
    this._abortController = null;
    this._el = null;
    this._textareaEl = null;
    this._unlisten = null;
    this._shortcutTarget = null;
    // Health check timer
    this._healthCheckTimer = null;
    // Nur automatische Läufe werden beim Lostippen abgebrochen.
    this._autoRun = false;
    this._userTextAtStart = undefined;
    this._wasAutoCancelled = false;
  }

  componentDidMount() {
    this._mounted = true;
    const { draft, session } = this.props;

    // Abbruch der Auto-Generierung, sobald der Nutzer selbst tippt.
    if (session && typeof session.listen === 'function') {
      this._unlisten = session.listen(this._onDraftChange);
    }

    // Tastenkürzel am umgebenden Composer.
    if (this._el) {
      this._shortcutTarget = this._el.closest('.composer-inner-wrap') || this._el;
      this._shortcutTarget.addEventListener('keydown', this._onShortcut);
    }

    // Health Check Timer starten (falls aktiviert)
    if (AIService.getConfig('healthCheckEnabled')) {
      this._startHealthCheckTimer();
    }

    if (
      this._isReply() &&
      AIService.isConfigured() &&
      AIService.getConfig('autoGenerate') &&
      !autoGeneratedFor.has(draft.headerMessageId)
    ) {
      autoGeneratedFor.add(draft.headerMessageId);
      this._generate({ auto: true });
    }
  }

  componentWillUnmount() {
    this._mounted = false;
    if (this._abortController) {
      this._abortController.abort();
    }
    if (this._healthCheckTimer) {
      clearInterval(this._healthCheckTimer);
      this._healthCheckTimer = null;
    }
    if (this._unlisten) {
      this._unlisten();
      this._unlisten = null;
    }
    if (this._shortcutTarget) {
      this._shortcutTarget.removeEventListener('keydown', this._onShortcut);
      this._shortcutTarget = null;
    }
  }

  // Health Check Timer
  _startHealthCheckTimer = () => {
    if (this._healthCheckTimer) clearInterval(this._healthCheckTimer);
    const interval = AIService.getConfig('healthCheckInterval') || 5 * 60 * 1000;
    // Initialer Check
    this._runHealthCheck();
    // Periodisch
    this._healthCheckTimer = setInterval(() => this._runHealthCheck(), interval);
  };

  _runHealthCheck = async () => {
    if (!this._mounted || !AIService.getConfig('healthCheckEnabled')) return;
    this.setState({ healthStatus: 'checking' });
    try {
      const result = await AIService.runPeriodicHealthCheck();
      if (this._mounted) {
        if (result.ok) {
          this.setState({ healthStatus: 'healthy', healthError: null });
          if (result.failover) {
            this.setState({ healthError: `Failover zu ${result.model} erfolgreich` });
          }
        } else {
          this.setState({ healthStatus: 'unhealthy', healthError: result.error });
        }
      }
    } catch (err) {
      if (this._mounted) {
        this.setState({ healthStatus: 'unhealthy', healthError: err.message });
      }
    }
  };

  componentDidUpdate(prevProps, prevState) {
    if (this._textareaEl && prevState.suggestion !== this.state.suggestion) {
      this._growTextarea(this._textareaEl);
    }
  }

  _onShortcut = (event) => {
    const shortcut = parseShortcut(AIService.getConfig('shortcutKey'));
    const ctrlMatch = shortcut.ctrl ? (event.ctrlKey || event.metaKey) : !(event.ctrlKey || event.metaKey);
    const shiftMatch = shortcut.shift ? event.shiftKey : !event.shiftKey;
    const altMatch = shortcut.alt ? event.altKey : !event.altKey;
    const metaMatch = shortcut.meta ? event.metaKey : !event.metaKey;
    const keyMatch = event.key && event.key.toLowerCase() === shortcut.key.toLowerCase();

    if (ctrlMatch && shiftMatch && altMatch && metaMatch && keyMatch) {
      event.preventDefault();
      event.stopPropagation();
      if (AIService.isConfigured() && this.state.status !== 'loading') {
        this._generate();
      }
    }
  };

  _onDraftChange = () => {
    if (this.state.status !== 'loading' || !this._autoRun) return;
    if (this._userTextAtStart === undefined) return;
    try {
      const { userText } = this._quoteAndUserText();
      if ((userText || '').trim() !== this._userTextAtStart) {
        // Der Nutzer schreibt selbst — der automatische Vorschlag wäre ohnehin
        // veraltet, also abbrechen (spart Wartezeit und API-Kosten).
        this._onCancel();
      }
    } catch (err) {
      // Panel darf den Composer nie stören.
    }
  };

  _growTextarea = (el) => {
    if (!el) return;
    this._textareaEl = el;
    el.style.height = 'auto';
    el.style.height = `${Math.min(el.scrollHeight + 2, 400)}px`;
  };

  _toggleCollapsed = () => {
    const collapsed = !this.state.collapsed;
    this.setState({ collapsed });
    AIService.setConfig('panelCollapsed', collapsed);
  };

  _isReply() {
    return !!this.props.draft.replyToHeaderMessageId;
  }

  // Immer den aktuellen Draft aus der Session lesen — das `draft`-Prop kann
  // hinter dem Editor-Zustand zurückhängen.
  _currentDraft() {
    const { session, draft } = this.props;
    if (session && typeof session.draft === 'function') {
      return session.draft() || draft;
    }
    return draft;
  }

  _quoteAndUserText() {
    const body = this._currentDraft().body || '';
    let insertion = body.length;
    for (const marker of INSERTION_MARKERS) {
      const i = body.indexOf(marker);
      if (i !== -1) insertion = Math.min(insertion, i);
    }
    return {
      userText: htmlToText(body.substr(0, insertion)),
      quotedText: htmlToText(body.substr(insertion)),
    };
  }

  // options: { previousSuggestion, refineInstruction } für Überarbeitungen,
  // { auto: true } für die automatische Generierung beim Öffnen.
  _generate = async (options = {}) => {
    const { previousSuggestion, refineInstruction } = options;
    const { session } = this.props;
    const isReply = this._isReply();
    const isRefine = !!(previousSuggestion && refineInstruction);
    this._autoRun = !!options.auto;
    this._userTextAtStart = undefined;
    this.setState({
      status: 'loading',
      error: null,
      partial: '',
      // Beim Verfeinern bleibt ein evtl. vorhandener Betreffvorschlag stehen.
      subjectSuggestion: isRefine ? this.state.subjectSuggestion : null,
    });

    if (this._abortController) {
      this._abortController.abort();
    }
    this._abortController = new AbortController();

    // Mailspring überträgt getippten Text nur verzögert vom Editor in
    // draft.body. Ohne Commit fehlen gerade erst geschriebene Stichpunkte
    // im gelesenen Body.
    try {
      if (session && session.changes && typeof session.changes.commit === 'function') {
        await session.changes.commit();
      }
    } catch (err) {
      // Notfalls mit dem letzten bekannten Body weiterarbeiten.
    }

    const draft = this._currentDraft();
    const { userText, quotedText } = this._quoteAndUserText();
    this._userTextAtStart = (userText || '').trim();

    // Kompletten Thread aus der Datenbank laden; schlägt das fehl, dient das
    // Zitat im Entwurf weiterhin als Kontext.
    let threadContext = null;
    let threadCount = 0;
    if (isReply && AIService.getConfig('sendThreadContext')) {
      try {
        const loaded = await loadThreadContext(draft);
        if (loaded) {
          threadContext = loaded.text;
          threadCount = loaded.messageCount;
        }
      } catch (err) {
        threadContext = null;
      }
    }

    // Sichtbar machen, welcher Kontext tatsächlich mitgeschickt wird.
    this.setState({
      contextInfo: {
        threadCount,
        usedQuote: isReply && !threadContext,
        hasUserText: !!(userText && userText.trim()),
      },
    });

    const sender = (draft.from && draft.from[0]) || null;
    const wantSubject = !isReply && !isRefine && !(draft.subject || '').trim();

    AIService.generateReply({
      subject: draft.subject,
      recipient: (draft.to || []).map((c) => c.toString()).join(', '),
      senderName: sender ? sender.name || sender.email : null,
      quotedText,
      userText,
      threadContext,
      isReply,
      tone: this.state.tone === 'standard' ? null : this.state.tone,
      wantSubject,
      previousSuggestion,
      refineInstruction,
      signal: this._abortController.signal,
      includeAttachments: AIService.getConfig('includeAttachments'),
      draft,
      onToken: (text) => {
        if (!this._mounted) return;
        if (wantSubject) {
          const split = AIService.splitSubject(text);
          this.setState({
            partial: split.body,
            subjectSuggestion: split.subject || this.state.subjectSuggestion,
          });
        } else {
          this.setState({ partial: text });
        }
      },
    })
      .then((result) => {
        if (this._mounted) {
          if (wantSubject) {
            const split = AIService.splitSubject(result.text);
            this.setState({
              status: 'done',
              suggestion: split.body,
              subjectSuggestion: split.subject,
              tokenUsage: result.usage,
            }, () => this._addToHistory(split.body));
          } else {
            this.setState({ status: 'done', suggestion: result.text, tokenUsage: result.usage }, () => this._addToHistory(result.text));
          }
        }
        this._abortController = null;
      })
      .catch((err) => {
        if (this._mounted) {
          if (err.message === 'Generierung abgebrochen.') {
            return;
          }
          this.setState({ status: 'error', error: err.message });
        }
        this._abortController = null;
      });
  };

  // History/Undo methods
  _addToHistory = (suggestion) => {
    const { suggestionHistory, historyIndex } = this.state;
    // Remove any entries after current index (if we undid and then generate new)
    const newHistory = suggestionHistory.slice(0, historyIndex + 1);
    newHistory.push(suggestion);
    // Keep max 3 entries
    if (newHistory.length > 3) newHistory.shift();
    this.setState({
      suggestionHistory: newHistory,
      historyIndex: newHistory.length - 1,
    });
  };

  _undo = () => {
    const { suggestionHistory, historyIndex } = this.state;
    if (historyIndex > 0) {
      const newIndex = historyIndex - 1;
      this.setState({
        suggestion: suggestionHistory[newIndex],
        historyIndex: newIndex,
      });
    }
  };

  _redo = () => {
    const { suggestionHistory, historyIndex } = this.state;
    if (historyIndex < suggestionHistory.length - 1) {
      const newIndex = historyIndex + 1;
      this.setState({
        suggestion: suggestionHistory[newIndex],
        historyIndex: newIndex,
      });
    }
  };

  _onCancel = () => {
    const wasAuto = this._autoRun;
    this._autoRun = false;
    this._wasAutoCancelled = wasAuto;
    if (this._abortController) {
      this._abortController.abort();
      this._abortController = null;
    }
    this.setState({ status: 'idle', suggestion: null, error: null, tokenUsage: null });
  };

  _onInsert = (replace = false) => {
    const { session } = this.props;
    const current = session.draft().body;
    let insertion = current.length;
    for (const marker of INSERTION_MARKERS) {
      const i = current.indexOf(marker);
      if (i !== -1) insertion = Math.min(insertion, i);
    }
    const html = textToHtml(this.state.suggestion);
    const prefix = replace ? '' : current.substr(0, insertion);
    const changes = { body: prefix + html + current.substr(insertion) };
    // Betreffvorschlag übernehmen, sofern der Nutzer noch keinen gesetzt hat.
    if (this.state.subjectSuggestion && !(session.draft().subject || '').trim()) {
      changes.subject = this.state.subjectSuggestion;
    }
    session.changes.add(changes);
    this.setState({ status: 'idle', suggestion: null, subjectSuggestion: null, tokenUsage: null, suggestionHistory: [], historyIndex: -1 });
  };

  _onDismiss = () => {
    this.setState({ status: 'idle', suggestion: null, error: null, subjectSuggestion: null, tokenUsage: null, suggestionHistory: [], historyIndex: -1 });
  };

  _onRefine = () => {
    const instruction = this.state.refineText.trim();
    const previous = this.state.suggestion;
    if (!instruction || !previous) return;
    this.setState({ refineText: '' });
    this._generate({ previousSuggestion: previous, refineInstruction: instruction });
  };

  _renderToneChips() {
    const tones = [
      ['standard', 'Standard'],
      ['formell', 'Formell'],
      ['locker', 'Locker'],
      ['kurz', 'Kurz'],
    ];
    return e(
      'div',
      { className: 'ai-tone-row' },
      e('span', { className: 'ai-tone-label' }, 'Ton:'),
      tones.map(([key, label]) =>
        e(
          'button',
          {
            key,
            className: 'ai-tone-chip' + (this.state.tone === key ? ' ai-tone-chip-active' : ''),
            onClick: () => this.setState({ tone: key }),
          },
          label
        )
      )
    );
  }

  _renderContextInfo() {
    const info = this.state.contextInfo;
    if (!info) return null;
    const parts = [];
    if (this._isReply()) {
      parts.push(
        info.threadCount > 0
          ? `Verlauf: ${info.threadCount} Nachricht${info.threadCount === 1 ? '' : 'en'}`
          : info.usedQuote
            ? 'Kontext: Zitat aus dem Entwurf'
            : 'Kontext: nur Betreff/Empfänger'
      );
      parts.push(info.hasUserText ? 'Deine Stichpunkte: ✓' : 'Deine Stichpunkte: keine');
    } else {
      parts.push(info.hasUserText ? 'Deine Vorgaben: ✓' : 'Keine Vorgaben — nur Betreff');
    }
    return e('div', { className: 'ai-context-info' }, `Gesendet — ${parts.join(' · ')}`);
  }

  _openPreferences = () => {
    Actions.openPreferences();
    Actions.switchPreferencesTab('AI Drafts');
  };

  render() {
    const { collapsed } = this.state;
    return e(
      'div',
      {
        className: 'ai-reply-drafts-panel' + (collapsed ? ' ai-collapsed' : ''),
        ref: (el) => {
          this._el = el;
        },
      },
      e(
        'div',
        { className: 'ai-panel-header', onClick: this._toggleCollapsed },
        e('span', { className: 'ai-panel-title' }, '✨ AI Drafts'),
        e('span', { className: 'ai-panel-toggle' }, collapsed ? '▸' : '▾')
      ),
      collapsed ? null : this._renderBody()
    );
  }

  _renderBody() {
    if (!AIService.isConfigured()) {
      return e(
        'div',
        { className: 'ai-panel-body ai-hint' },
        'AI Drafts ist noch nicht konfiguriert. ',
        e(
          'a',
          { onClick: this._openPreferences },
          'Einstellungen → AI Drafts öffnen'
        )
      );
    }

    const { status, suggestion, error } = this.state;
    const isReply = this._isReply();

    if (status === 'idle') {
      return e(
        'div',
        { className: 'ai-panel-body ai-idle' },
        this._wasAutoCancelled && e(
          'div',
          { className: 'ai-auto-cancelled-hint' },
          'Automatisch abgebrochen (Sie tippen selbst). Klicken Sie auf Generieren für neuen Vorschlag.'
        ),
        e(
          'div',
          { className: 'ai-actions' },
          e(
            'button',
            {
              className: 'btn btn-emphasis',
              title: 'Tastenkürzel: ' + (AIService.getConfig('shortcutKey') || 'Strg+Umschalt+G'),
              onClick: () => this._generate(),
            },
            isReply ? '✨ KI-Antwort generieren' : '✨ KI-Entwurf generieren'
          ),
          this._renderToneChips()
        )
      );
    }

    if (status === 'loading') {
      const { partial } = this.state;
      return e(
        'div',
        { className: 'ai-panel-body ai-loading' },
        e(
          'div',
          { className: 'ai-loading-container' },
          e('div', { className: 'ai-spinner' }),
          e(
            'span',
            { className: 'ai-loading-text' },
            isReply ? 'Antwort wird generiert…' : 'Entwurf wird generiert…'
          )
        ),
        this._renderContextInfo(),
        partial
          ? e('div', { className: 'ai-suggestion-stream' }, partial)
          : null,
        e(
          'div',
          { className: 'ai-actions' },
          e('button', { className: 'btn btn-cancel', onClick: this._onCancel }, 'Abbrechen')
        )
      );
    }

    if (status === 'error') {
      return e(
        'div',
        { className: 'ai-panel-body ai-error' },
        e('div', { className: 'ai-error-message' }, `Fehler: ${error}`),
        e(
          'div',
          { className: 'ai-error-hint' },
          'Tipp: Prüfe URL, API-Key und Modell mit „Modelle laden / Verbindung testen“ in den ',
          e('a', { onClick: this._openPreferences }, 'Einstellungen → AI Drafts'),
          '. Details stehen in der Entwicklerkonsole (Ansicht → Entwickler).'
        ),
        e(
          'div',
          { className: 'ai-actions' },
          e('button', { className: 'btn', onClick: () => this._generate() }, 'Erneut versuchen'),
          e('button', { className: 'btn', onClick: this._onDismiss }, 'Verwerfen')
        )
      );
    }

    const { userText } = this._quoteAndUserText();
    const hasUserText = userText && userText.trim().length > 0;
    const { suggestionHistory, historyIndex } = this.state;
    const canUndo = historyIndex > 0;
    const canRedo = historyIndex < suggestionHistory.length - 1;

    return e(
      'div',
      { className: 'ai-panel-body ai-suggestion' },
      e('div', { className: 'ai-suggestion-label' }, 'KI-Entwurfsvorschlag (bearbeitbar)'),
      // Token usage / cost display
      this.state.tokenUsage ? e(
        'div',
        { className: 'ai-token-usage' },
        this.state.tokenUsage.unknown ? 'Token-Kosten: Unbekanntes Modell' :
        this.state.tokenUsage.free ? 'Lokales / kostenloses Modell — ca. 0.00 $' :
        this.state.tokenUsage.local ? `Lokales Modell — ca. ${this.state.tokenUsage.inputTokens.toLocaleString()} in / ${this.state.tokenUsage.outputTokens.toLocaleString()} out Tokens` :
        `≈ ${this.state.tokenUsage.inputTokens.toLocaleString()} in / ${this.state.tokenUsage.outputTokens.toLocaleString()} out Tokens ≈ ${this.state.tokenUsage.cost.toFixed(4)} $`
      ) : null,
      // Health status indicator
      this.state.healthStatus === 'unhealthy' ? e(
        'div',
        { className: 'ai-health-status ai-health-unhealthy' },
        '⚠ Backend nicht erreichbar: ',
        this.state.healthError || 'Unbekannter Fehler',
        this.state.failoverEnabled ? ' — Failover aktiv' : null
      ) : null,
      this.state.healthStatus === 'healthy' && this.state.healthError ? e(
        'div',
        { className: 'ai-health-status ai-health-failover' },
        '✓ ',
        this.state.healthError
      ) : null,
      this._renderContextInfo(),
      this.state.subjectSuggestion
        ? e(
            'div',
            { className: 'ai-subject-suggestion' },
            `Betreffvorschlag: „${this.state.subjectSuggestion}“ — wird beim Einfügen übernommen, solange der Betreff leer ist.`
          )
        : null,
      e('textarea', {
        className: 'ai-suggestion-text-edit',
        value: suggestion || '',
        ref: this._growTextarea,
        onChange: (event) => {
          this.setState({ suggestion: event.target.value });
          this._growTextarea(event.target);
        },
      }),
      e(
        'div',
        { className: 'ai-actions' },
        hasUserText
          ? [
              e(
                'button',
                {
                  key: 'replace',
                  className: 'btn btn-emphasis',
                  onClick: () => this._onInsert(true),
                },
                'Ersetzen'
              ),
              e(
                'button',
                {
                  key: 'append',
                  className: 'btn',
                  onClick: () => this._onInsert(false),
                },
                'Anhängen'
              ),
            ]
          : e(
              'button',
              { className: 'btn btn-emphasis', onClick: () => this._onInsert(false) },
              'Einfügen'
            ),
        e('button', { className: 'btn', onClick: () => this._generate() }, 'Neu generieren'),
        // Undo/Redo buttons
        canUndo && e(
          'button',
          { className: 'btn', onClick: () => this._undo(), title: 'Rückgängig (Strg+Z)' },
          '↶ Rückgängig'
        ),
        canRedo && e(
          'button',
          { className: 'btn', onClick: () => this._redo(), title: 'Wiederherstellen (Strg+Umschalt+Z)' },
          '↷ Wiederherstellen'
        ),
        e('button', { className: 'btn', onClick: this._onDismiss }, 'Verwerfen'),
        this._renderToneChips()
      ),
      e(
        'div',
        { className: 'ai-refine-row' },
        e('input', {
          type: 'text',
          className: 'ai-refine-input',
          placeholder: 'Änderungswunsch, z.B. „kürzer“ oder „erwähne noch …“',
          value: this.state.refineText,
          onChange: (event) => this.setState({ refineText: event.target.value }),
          onKeyDown: (event) => {
            if (event.key === 'Enter') {
              event.preventDefault();
              this._onRefine();
            }
          },
        }),
        e(
          'button',
          {
            className: 'btn',
            disabled: !this.state.refineText.trim(),
            onClick: this._onRefine,
          },
          'Überarbeiten'
        )
      )
    );
  }
}

AiDraftPanel.displayName = 'AiDraftPanel';
AiDraftPanel.propTypes = {
  draft: PropTypes.object.isRequired,
  session: PropTypes.object.isRequired,
};

module.exports = AiDraftPanel;