/* global AppEnv */

const { React, PropTypes, Actions } = require('mailspring-exports');
const AIService = require('./ai-service');

const e = React.createElement;

// Merkt sich Drafts, für die bereits automatisch generiert wurde, damit ein
// Re-Mount des Composers keinen zweiten API-Call auslöst.
const autoGeneratedFor = new Set();

// Marker, vor denen eingefügt wird — identisch zur Logik des internen
// Templates-Plugins von Mailspring.
const INSERTION_MARKERS = [
  '<signature',
  '<div class="gmail_quote_attribution"',
  '<blockquote class="gmail_quote"',
];

function escapeHtml(text) {
  return text
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;');
}

function textToHtml(text) {
  return (
    text
      .split('\n')
      .map((line) => `<div>${escapeHtml(line) || '<br/>'}</div>`)
      .join('') + '<br/>'
  );
}

function htmlToText(html) {
  const withBreaks = html
    .replace(/<br[^>]*>/gi, '\n')
    .replace(/<\/(p|div|blockquote|li)>/gi, '\n')
    .replace(/<style[\s\S]*?<\/style>/gi, '')
    .replace(/<script[\s\S]*?<\/script>/gi, '');
  const el = document.createElement('div');
  el.innerHTML = withBreaks;
  return (el.textContent || '').replace(/\n{3,}/g, '\n\n').trim();
}

class AiDraftPanel extends React.Component {
  constructor(props) {
    super(props);
    this.state = { status: 'idle', suggestion: null, error: null };
    this._mounted = false;
  }

  componentDidMount() {
    this._mounted = true;
    const { draft } = this.props;
    if (
      this._isReply() &&
      AIService.isConfigured() &&
      AIService.getConfig('autoGenerate') &&
      !autoGeneratedFor.has(draft.headerMessageId)
    ) {
      autoGeneratedFor.add(draft.headerMessageId);
      this._generate();
    }
  }

  componentWillUnmount() {
    this._mounted = false;
  }

  _isReply() {
    return !!this.props.draft.replyToHeaderMessageId;
  }

  _quoteAndUserText() {
    const body = this.props.draft.body || '';
    let insertion = body.length;
    for (const marker of INSERTION_MARKERS) {
      const i = body.indexOf(marker);
      if (i !== -1) insertion = Math.min(insertion, i);
    }
    return {
      userText: htmlToText(body.substr(0, insertion)),
      quotedText: htmlToText(body.substr(insertion)),
    };
  }

  _generate = () => {
    const { draft } = this.props;
    const { userText, quotedText } = this._quoteAndUserText();
    this.setState({ status: 'loading', error: null });

    AIService.generateReply({
      subject: draft.subject,
      recipient: (draft.to || []).map((c) => c.toString()).join(', '),
      quotedText,
      userText,
    })
      .then((suggestion) => {
        if (this._mounted) this.setState({ status: 'done', suggestion });
      })
      .catch((err) => {
        if (this._mounted) this.setState({ status: 'error', error: err.message });
      });
  };

  _onInsert = () => {
    const { session } = this.props;
    const current = session.draft().body;
    let insertion = current.length;
    for (const marker of INSERTION_MARKERS) {
      const i = current.indexOf(marker);
      if (i !== -1) insertion = Math.min(insertion, i);
    }
    const html = textToHtml(this.state.suggestion);
    session.changes.add({
      body: current.substr(0, insertion) + html + current.substr(insertion),
    });
    this.setState({ status: 'idle', suggestion: null });
  };

  _onDismiss = () => {
    this.setState({ status: 'idle', suggestion: null, error: null });
  };

  _openPreferences = () => {
    Actions.openPreferences();
    Actions.switchPreferencesTab('AI Drafts');
  };

  render() {
    if (!this._isReply()) return null;

    if (!AIService.isConfigured()) {
      return e(
        'div',
        { className: 'ai-reply-drafts-panel ai-hint' },
        'AI Drafts ist noch nicht konfiguriert. ',
        e(
          'a',
          { onClick: this._openPreferences },
          'Einstellungen → AI Drafts öffnen'
        )
      );
    }

    const { status, suggestion, error } = this.state;

    if (status === 'idle') {
      return e(
        'div',
        { className: 'ai-reply-drafts-panel ai-idle' },
        e(
          'button',
          { className: 'btn', onClick: this._generate },
          '✨ KI-Antwort generieren'
        )
      );
    }

    if (status === 'loading') {
      return e(
        'div',
        { className: 'ai-reply-drafts-panel ai-loading' },
        'Antwort wird generiert…'
      );
    }

    if (status === 'error') {
      return e(
        'div',
        { className: 'ai-reply-drafts-panel ai-error' },
        e('div', { className: 'ai-error-message' }, `Fehler: ${error}`),
        e(
          'div',
          { className: 'ai-actions' },
          e('button', { className: 'btn', onClick: this._generate }, 'Erneut versuchen'),
          e('button', { className: 'btn', onClick: this._onDismiss }, 'Verwerfen')
        )
      );
    }

    return e(
      'div',
      { className: 'ai-reply-drafts-panel ai-suggestion' },
      e('div', { className: 'ai-suggestion-label' }, 'KI-Antwortvorschlag'),
      e('div', { className: 'ai-suggestion-text' }, suggestion),
      e(
        'div',
        { className: 'ai-actions' },
        e('button', { className: 'btn btn-emphasis', onClick: this._onInsert }, 'Einfügen'),
        e('button', { className: 'btn', onClick: this._generate }, 'Neu generieren'),
        e('button', { className: 'btn', onClick: this._onDismiss }, 'Verwerfen')
      )
    );
  }
}

AiDraftPanel.displayName = 'AiDraftPanel';
AiDraftPanel.propTypes = {
  draft: PropTypes.object.isRequired,
  session: PropTypes.object.isRequired,
};

module.exports = AiDraftPanel;
